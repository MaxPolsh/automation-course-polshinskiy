package org.example.homework.hw5;



public class UpperLowerCase {
    public static void main(String[] args) {
        String text = "we will rock you";
        String toUpperCase = text.toUpperCase();
        System.out.println("Upper case:"+ toUpperCase);
        String toLowerCase = toUpperCase.toLowerCase();
        System.out.println("Lower case:" + toLowerCase);


    }
}
